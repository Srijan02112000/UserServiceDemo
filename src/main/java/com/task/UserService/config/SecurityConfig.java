@Slf4j
@Configuration
@EnableWebSecurity
public class SecurityConfig {

    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        http
                .csrf(csrf -> csrf.disable())
                .authorizeHttpRequests(auth -> auth
                        .requestMatchers("/api/v1/users/delete").hasRole("ADMIN")
                        .requestMatchers("/api/v1/users/all").hasRole("ADMIN")
                        .requestMatchers(
                                new AntPathRequestMatcher("/swagger-ui/**"),
                                new AntPathRequestMatcher("/v3/api-docs/**"),
                                new AntPathRequestMatcher("/actuator/**")
                        ).permitAll()
                        .anyRequest().authenticated()
                )
                .httpBasic(Customizer.withDefaults())
                .sessionManagement(session -> session.sessionCreationPolicy(SessionCreationPolicy.STATELESS));

        return http.build();
    }


        @Bean
       public UserDetailsService userDetailsService() {
        log.info("Creating user details service");
        InMemoryUserDetailsManager in = new InMemoryUserDetailsManager();
        in.createUser(User.withUsername("admin").password("admin123").roles("ADMIN", "USER").build());
        in.createUser(User.withUsername("user").password("user123").roles("USER").build());
        return in;
//
    }
    @Bean
    public AuthenticationProvider authenticationProvider() {
        DaoAuthenticationProvider provider = new DaoAuthenticationProvider();
        provider.setPasswordEncoder(NoOpPasswordEncoder.getInstance());
        provider.setUserDetailsService(userDetailsService());

        return provider;
    }
    @Bean
    public RestTemplate restTemplate() {
        return new RestTemplate();
    }

    @Bean
    BCryptPasswordEncoder passwordEncoderBCrypt(){
        return new BCryptPasswordEncoder();
    }
}
